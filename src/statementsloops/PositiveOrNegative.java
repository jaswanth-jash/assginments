package statementsloops;

public class PositiveOrNegative {
    public static void main(String args[]) {
        int value = 21;
        if (value < 0) {
            System.out.println("The value is Negative: " + value);
        } else if (value > 0) {
            System.out.println("The value is Positive: " + value);
        } else {
            System.out.println("The value is Zero " + value);
        }
    }
}
