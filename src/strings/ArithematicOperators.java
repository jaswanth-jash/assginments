package strings;

public class ArithematicOperators {
    public static void main(String[] args) {
        int a = 20;
        int b = 15;
        int add, sub, mul, div, mod;
        add = a + b;
        sub = a - b;
        mul = a * b;
        div = a / b;
        mod = a % b;
        System.out.println("sum of the number " + add);
        System.out.println("sub of the number " + sub);
        System.out.println("mul of the number " + mul);
        System.out.println("div of the number " + div);
        System.out.println("mod of the number " + mod);
    }
}
