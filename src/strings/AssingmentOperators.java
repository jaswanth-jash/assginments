package strings;

public class AssingmentOperators {
    public static void main(String[] args) {
        int num1 = 25;
        int num2 = 14;
        System.out.println(num1 += num2);
        System.out.println(num1 -= num2);
        System.out.println(num1 *= num2);
        System.out.println(num1 /= num2);
        System.out.println(num1 %= num2);

    }
}
