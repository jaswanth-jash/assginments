package strings;

public class ComparisonOperator {
    public static void main(String args[]) {
        int a = 45;
        int b = 34;
        int c = 45;
        System.out.println(a == b);
        System.out.println(b != c);
        System.out.println(b > a);
        System.out.println(b < c);
        System.out.println(b >= c);
        System.out.println(a <= c);

    }
}
