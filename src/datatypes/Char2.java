package datatypes;

public class Char2 {
    public static void main(String args[]) {
        char ch1 = 'j';
        char ch2 = 'm';
        //casting or converting a charter into int type
        int ascii1 = (int) ch1;
        int ascii2 = (int) ch2;
        System.out.println("The ASCII value of " + ch1 + " is: " + ascii1);
        System.out.println("The ASCII value of " + ch2 + " is: " + ascii2);
    }
}
