package february;

public class DataType1 {
    public static void main(String[] args){
        String s = "Jaswanth"; // default value = null;
        Character c = 'G';
        int n = 21;
        float f = 9.87f;
        double d = 7.98;
        System.out.println(s);
        System.out.println(c);
        System.out.println(n);
        System.out.println(f);
        System.out.println(d);
    }
}
